<h3 class="page">Identify Your Needs</h3>
<img src="assets/responsive_mobile.png" alt="phone and tablet" class="hero" id="">

<p class="intro">With mobile devices accounting for 54.8 percent of web traffic world-wide in 2021 (Statista.com), it makes sense to put some thought into which method is best suited to your business.<br>
<br>
Examining your needs along with other factors such as time, cost, and user experience are key. Since responsive web and native app design are (nearly) mutually exclusive,<br>
the limited degree of interchangeability is likely to add a layer of complexity to your decision.<br>
<br>
While both methods affect the outcome of a crucial entity; the mobile iteration of your website. Beyond that though, lies a sharp contrast.<br>
<br>
To simplify, consider how these contrasted pros and cons weigh out against what you determine to be most important, then prioritize accordingly.</p>
<br>
<h3>Time Considerations</h3>
<p>Responsive design, according to Nick Babich of the Adobe XD blog “reduces time to market. The product team doesn’t need to go through approvals at the App Store for iOS or Google Play for Android (a mandatory step for every native mobile app). 
Thus, if you want to change the design of your website, you can simply update your code, and the changes will be immediately visible to your users. (Babich)”<br>
<br>
While a responsively designed website benefits over native app design for quick editing capabilites, as well as a reduced time to market, this method can affect time unfavorably. Responsive is known for long loading times,
which, according to Babich, can increase bounce rates.<br>
<br>
Raluca Budiu has a similar take. On the Nelsen Norman Group blog she writes, “Responsive sites tend to be slower. Although there are techniques for improving the performance of responsive sites,
because the same content is delivered to all types of devices, loading a responsive page can take longer than loading a mobile-dedicated page. (Budiu)”<br>
<br>
Regarding the techniques to remedy slow speeds in responsive sites, Budiu mentions adaptive design and progressive enhancement.</P>
<br>
<h3>Financial Considerations</h3>
<p>When budget is priority, a business may want to opt for responsive design. Natively-designed mobile apps are known to be significantly pricier than the alternative due to the higher degree of specialization involved with production along with a longer time to market.<br>
<br> 
“If you’re building a product from scratch, a responsive website will be much less expensive than a mobile app. Using lightweight, responsive frameworks like Bootstrap or Foundation make it easy to create a web app that looks good on any screen size. (Babich)”<br>
<br>
But what looks “good” to some is not always a universally accepted notion. Others, such as Kris with the smashstack.com blog, see functionality as the best look. He writes, “as they 
say, you get what you pay for. Responsive mobile sites, while necessary in today’s business climate, aren’t as fast or as functional as native mobile apps. (Kris)”</P>
