<h3>User Considerations</h3>
<p>Budiu writes that the average user will never know, or likely care if a website has been designed responsively.“For users there is no distinction among them.<br>
Normal people do not recognize responsive or adaptive sites as being different than mobile sites, and they don’t treat them differently. (Budiu)”<br>
<br>
She adds that regardless of the method used, that the same set of mobile-usability principles should be followed consistently throughout. 
Though with loading speeds of importance to the typical user, speed guidelines for each set of mobile-usability principles seem like an obvious inclusion to general best practices.</P>
<br>
<h3>Final Considerations</h3>
<p>With this as only a small preview the large set of advantages and disadvantages between responsive and mobile, insight into your specific situation is the probably the most important consideration of all.<br>
<br>
Your business’ needs, whatever those may be, can be realized somehow by deploying one technique or the other. Neither one should pose any sort of limitiation on your vision.</P>
